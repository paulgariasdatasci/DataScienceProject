def dayofweek(n):
    if n == 0:
        return 'Monday'
    elif n == 1:
        return 'Tuesday'
    elif n == 2:
        return 'Wednesday'
    elif n == 3:
        return 'Thursday'
    elif n == 4:
        return 'Friday'
    elif n == 5:
        return 'Saturday'
    elif n == 6:
        return 'Sunday'
